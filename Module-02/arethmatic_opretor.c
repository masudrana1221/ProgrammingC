
#include<stdio.h>
#include<math.h>

// floating arithmatic number

int main()
{
    float x , y;

    scanf("%f %f", &x, &y);

    float division = x / y;

    printf("Division : %.2f", division);

    return 0;
}


// integer arithmetic number

int main()
{
    int num1, num2;

    scanf("%d %d", &num1, &num2);

    int sum = num1 + num2 ;

    printf("Sum of the equation = %d", sum);
 
    return 0;
}



// simple arithmaric number


